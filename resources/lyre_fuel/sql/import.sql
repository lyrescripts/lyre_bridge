CREATE TABLE IF NOT EXISTS `lyre_fuel-stations` (
    `id` VARCHAR(50) NOT NULL,
    `name` VARCHAR(100) NOT NULL,
    `owner` VARCHAR(100) DEFAULT NULL,
    `account` INT DEFAULT 0,
    `state` ENUM('OPEN', 'CLOSED') DEFAULT 'OPEN',
    `sell_state` TINYINT(1) DEFAULT 0,
    `sell_price` INT DEFAULT 0,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `lyre_fuel-station_locations` (
    `id` INT AUTO_INCREMENT,
    `station_id` VARCHAR(50) NOT NULL,
    `location_type` ENUM('ped', 'pump', 'refill_vehicle') NOT NULL,
    `coords_x` FLOAT NOT NULL,
    `coords_y` FLOAT NOT NULL,
    `coords_z` FLOAT NOT NULL,
    `coords_w` FLOAT DEFAULT 0,
    `radius` FLOAT DEFAULT 2.0,
    `prop_key` VARCHAR(50) DEFAULT NULL,
    `no_prop` TINYINT(1) DEFAULT 0,
    `fuel_types` TEXT DEFAULT NULL,
    `use_nozzle` TINYINT(1) DEFAULT 1,
    PRIMARY KEY (`id`),
    INDEX `idx_station_id` (`station_id`),
    INDEX `idx_location_type` (`location_type`),
    CONSTRAINT `fk_locations_station` FOREIGN KEY (`station_id`)
        REFERENCES `lyre_fuel-stations`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `lyre_fuel-station_config` (
    `station_id` VARCHAR(50) NOT NULL,
    `config_key` VARCHAR(50) NOT NULL,
    `config_value` TEXT,
    PRIMARY KEY (`station_id`, `config_key`),
    CONSTRAINT `fk_config_station` FOREIGN KEY (`station_id`)
        REFERENCES `lyre_fuel-stations`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `lyre_fuel-station_fuel_types` (
    `id` INT AUTO_INCREMENT,
    `station_id` VARCHAR(50) NOT NULL,
    `fuel_type` VARCHAR(50) NOT NULL,
    `price_per_unit` FLOAT DEFAULT 2.0,
    `current_stock` INT DEFAULT 1000,
    `max_stock` INT DEFAULT 1000,
    `tank_level` INT DEFAULT 0,
    `sell_items` TINYINT(1) DEFAULT 1,
    `item_price` INT DEFAULT NULL,
    PRIMARY KEY (`id`),
    UNIQUE KEY `uk_station_fuel_type` (`station_id`, `fuel_type`),
    INDEX `idx_station_id` (`station_id`),
    INDEX `idx_fuel_type` (`fuel_type`),
    CONSTRAINT `fk_fuel_types_station` FOREIGN KEY (`station_id`)
        REFERENCES `lyre_fuel-stations`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `lyre_fuel-vehicle_filters` (
    `plate` VARCHAR(32) NOT NULL,
    `kilometers_since_replace` FLOAT DEFAULT 0,
    `last_replaced_mileage` FLOAT DEFAULT NULL,
    `last_replaced_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`plate`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `lyre_fuel-vehicle_injectors` (
    `plate` VARCHAR(32) NOT NULL,
    `kilometers_since_replace` FLOAT DEFAULT 0,
    `last_replaced_mileage` FLOAT DEFAULT NULL,
    `last_replaced_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`plate`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `lyre_fuel-vehicle_lpg` (
    `plate` VARCHAR(32) NOT NULL,
    `tank_size` FLOAT DEFAULT NULL,
    `fuel` FLOAT DEFAULT 0,
    `vaporizer_installed` TINYINT(1) DEFAULT 0,
    `filter_installed` TINYINT(1) DEFAULT 0,
    `injectors_installed` TINYINT(1) DEFAULT 0,
    `filter_kilometers` FLOAT DEFAULT 0,
    `injectors_kilometers` FLOAT DEFAULT 0,
    `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`plate`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;


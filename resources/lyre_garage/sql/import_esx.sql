-- =====================================================
-- LYRE GARAGE - ESX FRAMEWORK SQL IMPORT
-- =====================================================
-- This file adds columns to the ESX owned_vehicles table
-- and creates the framework-agnostic tables for garage management
-- =====================================================

-- Add lyre_garage columns to owned_vehicles table
ALTER TABLE `owned_vehicles`
    ADD COLUMN IF NOT EXISTS `lyre_garage-name` VARCHAR(100) DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-location` VARCHAR(100) DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-location_name` VARCHAR(100) DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-impound_fee` INT DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-allow_owner_retrieve` TINYINT(1) DEFAULT 1,
    ADD COLUMN IF NOT EXISTS `lyre_garage-impound_min_retrieve_date` BIGINT DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-impound_reason` TEXT DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-impounded_by` VARCHAR(255) DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-impounded_at` BIGINT DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-job_vehicle` TINYINT(1) DEFAULT 0,
    ADD COLUMN IF NOT EXISTS `lyre_garage-gang_vehicle` TINYINT(1) DEFAULT 0,
    ADD COLUMN IF NOT EXISTS `lyre_garage-job_rank` INT DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-gang_rank` INT DEFAULT NULL,
    ADD COLUMN IF NOT EXISTS `lyre_garage-fuel` DECIMAL(5,2) DEFAULT 100.00;

-- Add indexes for performance on lyre_garage columns
ALTER TABLE `owned_vehicles`
    ADD INDEX IF NOT EXISTS `idx_lyre_location_name` (`lyre_garage-location_name`),
    ADD INDEX IF NOT EXISTS `idx_lyre_location` (`lyre_garage-location`),
    ADD INDEX IF NOT EXISTS `idx_lyre_job_vehicle` (`lyre_garage-job_vehicle`),
    ADD INDEX IF NOT EXISTS `idx_lyre_gang_vehicle` (`lyre_garage-gang_vehicle`),
    ADD INDEX IF NOT EXISTS `idx_lyre_location_owner` (`lyre_garage-location`, `owner`);

-- Add indexes for performance on framework columns
ALTER TABLE `owned_vehicles`
    ADD INDEX IF NOT EXISTS `idx_lyre_plate` (`plate`),
    ADD INDEX IF NOT EXISTS `idx_lyre_owner` (`owner`),
    ADD INDEX IF NOT EXISTS `idx_lyre_owner_job_gang` (`owner`, `lyre_garage-job_vehicle`, `lyre_garage-gang_vehicle`);

-- =====================================================
-- SHARED ACCESS TABLE (framework-agnostic)
-- =====================================================
CREATE TABLE IF NOT EXISTS `lyre_garage-shared_access` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `plate` VARCHAR(100) NOT NULL,
    `owner_identifier` VARCHAR(255) NOT NULL,
    `shared_with_identifier` VARCHAR(255) NOT NULL,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE KEY `unique_shared_access` (`plate`, `shared_with_identifier`),
    INDEX `idx_plate` (`plate`),
    INDEX `idx_owner` (`owner_identifier`),
    INDEX `idx_shared_with` (`shared_with_identifier`)
) ENGINE=InnoDB;

ALTER TABLE `lyre_garage-shared_access`
    ADD INDEX IF NOT EXISTS `idx_shared_with_plate` (`shared_with_identifier`, `plate`);

ALTER TABLE `users`
    ADD INDEX IF NOT EXISTS `idx_lyre_users_identifier` (`identifier`);

-- =====================================================
-- GARAGE MANAGEMENT TABLES (framework-agnostic)
-- =====================================================
CREATE TABLE IF NOT EXISTS `lyre_garage-garages` (
    `id` VARCHAR(100) PRIMARY KEY,
    `name` VARCHAR(255) NOT NULL,
    `garage_type` ENUM('public', 'job', 'gang', 'impound') NOT NULL,
    `vehicle_type` ENUM('automobile', 'boat', 'heli', 'plane') NOT NULL,
    `main_job` VARCHAR(100) DEFAULT NULL,
    `main_gang` VARCHAR(100) DEFAULT NULL,
    `manager_rank` INT DEFAULT NULL,
    `job_garage_type` ENUM('owned_garage', 'spawn_garage') DEFAULT NULL,
    `blip_sort` INT NOT NULL DEFAULT 0,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX `idx_garage_type` (`garage_type`),
    INDEX `idx_vehicle_type` (`vehicle_type`),
    INDEX `idx_main_job` (`main_job`),
    INDEX `idx_main_gang` (`main_gang`)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS `lyre_garage-garage_locations` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `garage_id` VARCHAR(100) NOT NULL,
    `location_type` ENUM('ped', 'park', 'takeout') NOT NULL,
    `coords_x` FLOAT NOT NULL,
    `coords_y` FLOAT NOT NULL,
    `coords_z` FLOAT NOT NULL,
    `coords_w` FLOAT DEFAULT NULL,
    `radius` FLOAT DEFAULT NULL,
    `sort_order` INT NOT NULL DEFAULT 0,
    FOREIGN KEY (`garage_id`) REFERENCES `lyre_garage-garages`(`id`) ON DELETE CASCADE,
    INDEX `idx_garage_id` (`garage_id`),
    INDEX `idx_location_type` (`location_type`)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS `lyre_garage-garage_config` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `garage_id` VARCHAR(100) NOT NULL,
    `config_key` VARCHAR(100) NOT NULL,
    `config_value` TEXT NOT NULL,
    FOREIGN KEY (`garage_id`) REFERENCES `lyre_garage-garages`(`id`) ON DELETE CASCADE,
    UNIQUE KEY `unique_garage_config` (`garage_id`, `config_key`),
    INDEX `idx_garage_id` (`garage_id`),
    INDEX `idx_config_key` (`config_key`)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS `lyre_garage-garage_vehicles` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `garage_id` VARCHAR(100) NOT NULL,
    `model` VARCHAR(100) NOT NULL,
    `plate` VARCHAR(100) DEFAULT NULL,
    `max_spawned` INT NOT NULL DEFAULT 1,
    `required_rank` INT NOT NULL DEFAULT 0,
    `label` VARCHAR(255) NOT NULL,
    `primary_color_r` INT DEFAULT NULL,
    `primary_color_g` INT DEFAULT NULL,
    `primary_color_b` INT DEFAULT NULL,
    `secondary_color_r` INT DEFAULT NULL,
    `secondary_color_g` INT DEFAULT NULL,
    `secondary_color_b` INT DEFAULT NULL,
    `customization` TEXT DEFAULT NULL,
    `sort_order` INT NOT NULL DEFAULT 0,
    FOREIGN KEY (`garage_id`) REFERENCES `lyre_garage-garages`(`id`) ON DELETE CASCADE,
    INDEX `idx_garage_id` (`garage_id`)
) ENGINE=InnoDB;

